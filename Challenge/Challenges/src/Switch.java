import java.util.Scanner;

public class Switch {
    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);

        int a = input.nextInt();

        switch (a) {
            case 1: {
                System.out.println("jan");

            }
            case 2: {
                System.out.println("Feb");
            }
            case 3: {
                System.out.println("mar");
            }
            case 4: {
                System.out.println("april");
            }


        }

    }
}
