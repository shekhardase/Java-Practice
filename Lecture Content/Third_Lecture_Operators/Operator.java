package Third_Lecture_Operators;

public class Operator {

    public static void main(String[] args) {
        System.out.println(5 + 2);
        System.out.println(5 - 2);
        System.out.println(5 * 2);
        System.out.println(5 / 2);
        System.out.println(5 % 2);

        double x = 2;
        double y  = 10;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
    }
}
